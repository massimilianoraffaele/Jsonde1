package com.jsonde.client.domain;
/**
 * 
 * @author admin
 *
 */
public class Clazz extends DomainObject {

    private Long superClassId;
    private String name;

    private Long classLoaderId;
    private Long codeSourceId;

    private long createCounter;
    private long collectCounter;
    private long totalCurrentSize;

    /**
     * 
     * @return
     */
    public Long getSuperClassId() {
        return superClassId;
    }

    /**
     * 
     * @param superClassId
     */
    public void setSuperClassId(Long superClassId) {
        this.superClassId = superClassId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getClassLoaderId() {
        return classLoaderId;
    }

    public void setClassLoaderId(Long classLoaderId) {
        this.classLoaderId = classLoaderId;
    }

    public Long getCodeSourceId() {
        return codeSourceId;
    }

    public void setCodeSourceId(Long codeSourceId) {
        this.codeSourceId = codeSourceId;
    }

    public long getCreateCounter() {
        return createCounter;
    }

    public void setCreateCounter(long createCounter) {
        this.createCounter = createCounter;
    }

    public long getCollectCounter() {
        return collectCounter;
    }

    public void setCollectCounter(long collectCounter) {
        this.collectCounter = collectCounter;
    }

    public long getTotalCurrentSize() {
        return totalCurrentSize;
    }

    public void setTotalCurrentSize(long totalCurrentSize) {
        this.totalCurrentSize = totalCurrentSize;
    }

    @Override
    public String toString() {
        return "Clazz{" +
                "superClassId=" + superClassId +
                ", name='" + name + '\'' +
                '}';
    }

}
