package com.jsonde.gui.configuration;
/**
 * 
 * @author admin
 *
 */
public class SessionConfigurationException extends Exception {

	/**
	 * SessionConfigurationException
	 */
    public SessionConfigurationException() {
    }

    public SessionConfigurationException(String message) {
        super(message);
    }

    public SessionConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }

    public SessionConfigurationException(Throwable cause) {
        super(cause);
    }

}
